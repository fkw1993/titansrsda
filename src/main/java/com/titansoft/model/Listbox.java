package com.titansoft.model;

/**
 * 通用Listbox对象
 * 
 * @author hongcs
 */
public class Listbox {

	/**
	 * ID值
	 */
	private String id;
	/**
	 * 显示值
	 */
	private String text;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}

}